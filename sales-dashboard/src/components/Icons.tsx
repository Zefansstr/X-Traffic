export const getIcon = (name: string, props?: IconProps) => {
  switch (name.toLowerCase()) {
    case 'dashboard':
      return <DashboardIcon {...props} />;
    case 'operator':
      return <OperatorIcon {...props} />;
    case 'setting':
      return <SettingIcon {...props} />;
    case 'member':
      return <MemberIcon {...props} />;
    case 'commission':
      return <CommissionReportIcon {...props} />;
    case 'overall':
      return <OverallReportIcon {...props} />;
    case 'logout':
      return <LogoutIcon {...props} />;
    case 'login':
      return <LoginIcon {...props} />;
    default:
      return <DashboardIcon {...props} />;
  }
};
